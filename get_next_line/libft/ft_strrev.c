#include "libft.h"

char	*ft_strrev(char *str)
{
	int i;
	int n;
	int c;

	i = 0;
	n = ft_strlen(str) - 1;
	while (i < n)
	{
		c = str[i];
		str[i] = str[n];
		str[n] = c;
		i++;
		n--;
	}
	return (str);
}
