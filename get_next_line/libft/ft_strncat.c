#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	int		j;

	i = 0;
	j = 0;
	while (s1[j])
		j++;
	while (i < n)
	{
		s1[j] = s2[i];
		j++;
		i++;
	}
	return (s1);
}
