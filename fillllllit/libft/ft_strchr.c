#include <string.h>
#include "libft.h"

char	*ft_strchr(const char *str, char c)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] == c)
			return ((char*)&str[i]);
		i++;
	}
	if (c == '\0')
		return ((char*)&str[ft_strlen(str)]);
	return (NULL);
}
